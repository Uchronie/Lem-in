#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned int nb;

	nb = (n < 0) ? -n : n;
	(n < 0) ? ft_putchar('-') : n;
	if (nb > 9)
		ft_putnbr(nb / 10);
	ft_putchar(nb % 10 + 48);
}
