#include <stdlib.h>

void	ft_bzero(void *s, size_t n)
{
	while (n > 0)
	{
		if (n >= 8)
		{
			n -= 8;
			*((long*)(s + n)) ^= *((long*)(s + n));
		}
		else if (n >= 4)
		{
			n -= 4;
			*((int*)(s + n)) ^= *((int*)(s + n));
		}
		else
		{
			n--;
			*((char*)(s + n)) ^= *((char*)(s + n));
		}
	}
}
